package com.z.po;

public class ChooseCourse {
	private String CourseType;
	private String CourseNumber;
	private String MainCourseNumber;
	private String CourseCodeString;
	private String CourseName;
	private String CourseCredit;
	public String getCourseType() {
		return CourseType;
	}
	public void setCourseType(String courseType) {
		CourseType = courseType;
	}
	public String getCourseNumber() {
		return CourseNumber;
	}
	public void setCourseNumber(String courseNumber) {
		CourseNumber = courseNumber;
	}
	public String getMainCourseNumber() {
		return MainCourseNumber;
	}
	public void setMainCourseNumber(String mainCourseNumber) {
		MainCourseNumber = mainCourseNumber;
	}
	public String getCourseCodeString() {
		return CourseCodeString;
	}
	public void setCourseCodeString(String courseCodeString) {
		CourseCodeString = courseCodeString;
	}
	public String getCourseName() {
		return CourseName;
	}
	public void setCourseName(String courseName) {
		CourseName = courseName;
	}
	public String getCourseCredit() {
		return CourseCredit;
	}
	public void setCourseCredit(String courseCredit) {
		CourseCredit = courseCredit;
	}
	
}
